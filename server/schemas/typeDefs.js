const { gql } = require('apollo-server-express');

const typeDefs = gql`
  type User {
    _id: ID
    username: String
    email: String
    password: String
    permissions: [Permission]!
  }

  type Permission {
    _id: ID
    thoughtText: String
    thoughtAuthor: String
    createdAt: String
    comments: [Comment]!
  }

  type Comment {
    _id: ID
    commentText: String
    commentAuthor: String
    createdAt: String
  }

  type Auth {
    token: ID!
    user: User
  }

  type Query {
    users: [User]
    user(username: String!): User
    permissions(username: String): [Permission]
    permission(thoughtId: ID!): Permission
    me: User
  }

  type Mutation {
    addUser(username: String!, email: String!, password: String!): Auth
    login(email: String!, password: String!): Auth
    addThought(thoughtText: String!): Permission
    addComment(thoughtId: ID!, commentText: String!): Permission
    removeThought(thoughtId: ID!): Permission
    removeComment(thoughtId: ID!, commentId: ID!): Permission
  }
`;

module.exports = typeDefs;
